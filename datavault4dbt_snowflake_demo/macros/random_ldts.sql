{%- macro random_ldts() -%}
CASE uniform(1, 5, random()) 
    WHEN 1 THEN TO_TIMESTAMP('2022-10-01T07:00:00', 'YYYY-MM-DDTHH24:MI:SS') 
    WHEN 2 THEN TO_TIMESTAMP('2022-10-02T07:00:00', 'YYYY-MM-DDTHH24:MI:SS') 
    WHEN 3 THEN TO_TIMESTAMP('2022-10-03T07:00:00', 'YYYY-MM-DDTHH24:MI:SS') 
    WHEN 4 THEN TO_TIMESTAMP('2022-10-04T07:00:00', 'YYYY-MM-DDTHH24:MI:SS') 
    WHEN 5 THEN TO_TIMESTAMP('2022-10-05T07:00:00', 'YYYY-MM-DDTHH24:MI:SS') 
END
{%- endmacro -%}